package exercicio4;

public class Televisao {
	private int canal = 0;
	private int volume = 0;
	
	void setCanal(int novoCanal) {
		this.canal = novoCanal;
	}
	
	int getCanal() {
		return this.canal;
	}

	void setVolume(int novoVolume) {
		this.volume = novoVolume;
	}
	
	int getVolume() {
		return this.volume;
	}
}
